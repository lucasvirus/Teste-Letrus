/*11. Crie uma view que mostre todos os usuários que estão na cidade São Paulo e a nota final esteja entre 7 e 10 pontos */

	CREATE VIEW [dbo].[Vw_USUARIOS_SAO_PAULO_NOTA_FINAL_ENTRE_7_E_10]
	AS
	SELECT U.[nome]
			,E.[estado]
			,E.[cidade]
			,N.[nota_final]
		FROM [TesteLetrus].[dbo].[Usuarios] U
			INNER JOIN Endereco E
				ON E.id_usuario = U.id
			INNER JOIN Atividades A
				ON A.id_usuario = U.id
			INNER JOIN Nota N
				ON N.id_atividade = A.id
		WHERE E.cidade = 'SÃO PAULO'
		AND N.nota_final BETWEEN 7 AND 10
	GO